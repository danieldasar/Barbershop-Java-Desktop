    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("id_karyawan");
        tabel.addColumn("NAMA");
        tabel.addColumn("GENDER");
        tabel.addColumn("No. HP");
        tabel.addColumn("ALAMAT");
        try {
            String sql1 = "Select *from karyawan where id_karyawan like '%"+txtID_K.getText()
                    +"%' or nama_karyawan like '%"+txtNamaK.getText()
                    +"%' or gender like '%"+gender1.getText()
                    +"%' or phone like '%"+txtNoHP.getText()
                    +"%' or alamat like '%"+txtAlamat.getText()+"%'";
            Statement sttr = sambungan.createStatement();
            ResultSet res = sttr.executeQuery(sql1);
            while(res.next()){
                tabel.addRow(new Object[]{
                    res.getString("id_karyawan"),
                    res.getString("nama_karyawan"),
                    res.getString("gender"),
                    res.getString("phone"),
                    res.getString("alamat"),
                    res.getString("password")});
                jTable1.setModel(tabel);
            }
        }catch(Exception e){
            //System.out.println("ERROR");
            JOptionPane.showMessageDialog(null, "ERROR.. "+e);
        }
    }//GEN-LAST:event_cariActionPerformed

