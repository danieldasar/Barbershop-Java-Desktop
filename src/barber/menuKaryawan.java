    private void dataTable(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("id_karyawan");
        tabel.addColumn("NAMA");
        tabel.addColumn("GENDER");
        tabel.addColumn("No. HP");
        tabel.addColumn("ALAMAT");
        //jTable1.setModel(tbl);
        try {
            String sql1 = "Select *from Karyawan";
            Statement sttr = sambungan.createStatement();
            ResultSet res = sttr.executeQuery(sql1);
            while (res.next()){
                tabel.addRow(new Object []{
                    res.getString("id_karyawan"),
                    res.getString("nama_karyawan"),
                    res.getString("gender"),
                    res.getString("phone"),
                    res.getString("alamat"),
                });
                jTable1.setModel(tabel);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "DATA TABLE GAGAL..");
        }
    }
    
