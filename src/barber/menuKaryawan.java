    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String idK, namaK, noHP, jenkel, pass, alamat;
        idK = txtID_K.getText();
        namaK = txtNamaK.getText();
        if (gender1.isSelected()){
            jenkel = "L";
        }else{
            jenkel = "P";
        }
        noHP = txtNoHP.getText();
        pass = txtPass.getText();
        alamat = txtAlamat.getText();

        if (idK.equals("") | (namaK.equals("")) | (noHP.equals("")) | (pass.equals("")) | (alamat.equals(""))){
            JOptionPane.showMessageDialog(null, "Pengisisan Tak Boleh Kosong..");
            txtID_K.requestFocus();
        }else {
            try {
                String sql = "Insert Into karyawan values(?,?,?,?,?,?)";
                PreparedStatement stat = sambungan.prepareStatement(sql);
                try {
                    stat.setString(1, idK);
                    stat.setString(2, namaK);
                    stat.setString(3, jenkel);
                    stat.setString(4, noHP);
                    stat.setString(5, alamat);
                    stat.setString(6, pass);
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan..");
                    bersih();
                }catch(SQLException se){
                    System.out.println("Gagal Menyimpan.."+se);
                    JOptionPane.showMessageDialog(null, "Data yang anda masukkan ttelah ada.."
                        + "\nData baru gagal disimpan.. coba lagi.."
                        + "\n Pesan Error : \n"+se);
                    txtID_K.requestFocus();
                }
            }catch(Exception se){
                //System.out.println(se);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

